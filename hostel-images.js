function _normName(n) {
  return (n||'').toLowerCase().trim().replace(/[''`]/g,'').replace(/[,.\-_]+/g,' ').replace(/\s+/g,' ');
}

function getHostelImages(pgName) {
  if (!pgName) return null;
  if (HOSTEL_IMAGES[pgName] && HOSTEL_IMAGES[pgName].length) return HOSTEL_IMAGES[pgName];
  const norm = _normName(pgName);
  for (const [key, imgs] of Object.entries(HOSTEL_IMAGES)) {
    if (imgs.length && _normName(key) === norm) return imgs;
  }
  for (const [key, imgs] of Object.entries(HOSTEL_IMAGES)) {
    const kn = _normName(key);
    if (imgs.length && (kn.startsWith(norm) || norm.startsWith(kn))) return imgs;
  }
  return null;
}

